    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        String userid=jcCashierId.getSelectedItem().toString();
        
        try{
            boolean result=CashierDAO.deleteCashier(userid);
            
            if(result)
            {
                JOptionPane.showMessageDialog(null, "Cashier Deleted Successfully.");
                jcCashierId.removeItem(jcCashierId.getSelectedItem());
                if(jcCashierId.getItemCount()==0)
                {
                    btnRemove.setEnabled(false);
                    
                }
                return;
            }
         
            JOptionPane.showMessageDialog(null, "Cashier Not Deleted.");
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in remove cashier frame.");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveMouseClicked

