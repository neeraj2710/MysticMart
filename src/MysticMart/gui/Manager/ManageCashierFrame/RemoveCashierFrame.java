    private void loadCashieId() {
        try{
            List<String> cashierId=CashierDAO.getAllCashierUserId();
            if(cashierId.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"No Cashier Present!");
                return;
            }
            btnRemove.setEnabled(true);
            for(String userid:cashierId)
            {
                jcCashierId.addItem(userid);
            }
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in remove cashier frame.");
            ex.printStackTrace();
        }
    }
