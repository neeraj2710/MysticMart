    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        int result = validateInputs();
        if (result == 0) {
            JOptionPane.showMessageDialog(null, "Password must be atleast 4 characters long.");
            return;
        }
        if (result == -1) {
            JOptionPane.showMessageDialog(null, "User Id cannot be left blank.");
            return;
        }
        String pwd = String.valueOf(txtPassword.getPassword());
        String repwd = String.valueOf(txtRePassword.getPassword());
        if (isPasswordMatching(pwd, repwd) == false) {
            JOptionPane.showMessageDialog(null, "Password do not match.");
            return;
        }
        try {

            String empid = jcEmpId.getSelectedItem().toString();
            String userid = txtUserId.getText().trim();
            String username = txtEmpName.getText().trim();
            UserPojo u = new UserPojo();
            u.setEmpid(empid);
            u.setUserid(userid);
            u.setUsername(username);
            u.setPassword(pwd);
            u.setUsertype("Cashier");
            boolean status = CashierDAO.addCashier(u);
            if (status) {
                JOptionPane.showMessageDialog(null, "Cashier Added Successfully.");
                clearText();
                this.txtEmpName.setText("");
                jcEmpId.removeAllItems();
                loadCashierDetails();               
                return;
            }
            JOptionPane.showMessageDialog(null, "Error, Cashier not Added.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in adding cashier.");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddMouseClicked

