    private void loadCashierDetails() {
        try {

            cashier = CashierDAO.getNonRegisteredCashiers();
            if (cashier.isEmpty()) {              
                JOptionPane.showMessageDialog(null, "No unregistered cashier present.");
                btnAdd.setEnabled(false);
                return;
            }
            btnAdd.setEnabled(true);
            Set<String> keys = cashier.keySet();
            Iterator<String> it = keys.iterator();
            jcEmpId.removeAllItems();
            while (it.hasNext()) {
                jcEmpId.addItem(it.next());
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in getting cashier ID.");
            ex.printStackTrace();
        }
    }

    private void clearText() {
        txtPassword.setText("");
        txtRePassword.setText("");
        txtUserId.setText("");
         
    }
