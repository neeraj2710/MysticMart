    private void BtnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnUpdateMouseClicked
        if(!validateInputs())
        {
            return;
        }
        try{
            String userid=jcCashierId.getSelectedItem().toString();
            boolean result=CashierDAO.updatePassword(userid, password);
            if(result)
            {
              JOptionPane.showMessageDialog(null, "Password Changed Successfully.");
              clearText();
              jcCashierId.setSelectedIndex(0);
              return; 
            }
            JOptionPane.showMessageDialog(null, "Password Not Changed.");
            clearText();
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in while updating the password.");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_BtnUpdateMouseClicked

