    private void loadCashierDetails() {
        try{
        
            cashier=CashierDAO.getAllCashierIdAndName();
            if(cashier.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"No cashier present.");
                return;
            }
            
            Set<String> keys=cashier.keySet();
            for(String id:keys)
            {
                jcCashierId.addItem(id);
                
            }
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error in Update Cashier Frame");
            ex.printStackTrace();
        }
    }

    private void clearText() {
        txtPassword.setText("");
        txtRePassword.setText("");
    }
