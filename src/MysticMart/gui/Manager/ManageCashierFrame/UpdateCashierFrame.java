    private boolean validateInputs()
    {

        password=String.valueOf(txtPassword.getPassword());
        repassword=String.valueOf(txtRePassword.getPassword());
        if(password.length()==0 || repassword.length()==0)
        {
            JOptionPane.showMessageDialog(null, "Please, Input the password to be updated!");
            return false;
        }
        if(password.length()<4 || repassword.length()<4)
        {
            JOptionPane.showMessageDialog(null, "Password must be of atleast length 4.");
            return false;
        }
        if(!password.equals(repassword))
        {
            JOptionPane.showMessageDialog(null, "Password and Re-Entered Password must be same.");
            return false;
        }
        return true;
    }

